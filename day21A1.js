// Using let and const keywords
let firstName = "John";
let lastName = "Doe";
let address = "123 Main Street";
let city = "New York";
let country = "USA";
let age = 30;
let gender = "Male";

const fullName = `${firstName} ${lastName}`;

// Printing using concatenation and template literals
console.log("Full Name: " + fullName);
console.log(`Address: ${address}, ${city}, ${country}`);
console.log(`Age: ${age}, Gender: ${gender}`);
