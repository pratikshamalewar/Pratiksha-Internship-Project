// For loop
console.log("Using For Loop:");
for (let i = 10; i >= 0; i--) {
    console.log(i);
}

// While loop
console.log("Using While Loop:");
i = 10;
while (i >= 0) {
    console.log(i);
    i--;
}

// Do-While loop
console.log("Using Do-While Loop:");
i = 10;
do {
    console.log(i);
    i--;
} while (i >= 0);
