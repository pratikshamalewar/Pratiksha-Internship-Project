// For loop
console.log("Using For Loop:");
for (let i = 0; i <= 10; i++) {
    console.log(i);
}

// While loop
console.log("Using While Loop:");
let i = 0;
while (i <= 10) {
    console.log(i);
    i++;
}

// Do-While loop
console.log("Using Do-While Loop:");
i = 0;
do {
    console.log(i);
    i++;
} while (i <= 10);
